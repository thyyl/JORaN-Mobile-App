class Job {
  String jobID;
  String title;
  String location;
  String salary;
  String jobType;
  String experience;
  String educationLevel;
  String jobDescription;

  Job({
    required this.jobID,
    required this.title,
    required this.location,
    required this.salary,
    required this.jobType,
    required this.experience,
    required this.educationLevel,
    required this.jobDescription
  });
}